#include "ThreadSafe.hpp"
#include <iostream>

boost::mutex ThreadSafe::ioMutex;

void ThreadSafe::cerr(const string& s)
{
	LOCK(ioMutex);
	std::cerr << s << std::endl;
}


void ThreadSafe::cout(const string& s)
{
	LOCK(ioMutex);
	std::cout << s << std::endl;
}

